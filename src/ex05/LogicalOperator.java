package ex05;

public class LogicalOperator {
    public static void main(String[] args) {
        int x = 3;
        int y = 4;

        // 논리연산자(true or false)
        // && 그리고 (AND)
        // || 거나 (OR)
        System.out.println((x == 3) && (y == 7));
        System.out.println((x == 3) || (y == 4));
    }
}
